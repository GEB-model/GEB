def test_group_subbasins(sample_river_graph: nx.DiGraph) -> None:
    """Test grouping of subbasins based on area threshold."""
    groups = group_subbasins(sample_river_graph, max_area_m2=1e9)
    entries_in_groups = sum(len(subbasins) for subbasins in groups.values())
    assert entries_in_groups == len(sample_river_graph.nodes), (
        "Some subbasins were not assigned to any group."
    )

    x_coords = [
        sample_river_graph.nodes[subbasin]["yx"][0]
        for subbasin in sample_river_graph.nodes
    ]
    y_coords = [
        sample_river_graph.nodes[subbasin]["yx"][1]
        for subbasin in sample_river_graph.nodes
    ]
    rivers_xy = gpd.GeoDataFrame(
        {
            "subbasin_id": list(sample_river_graph.nodes),
            "geometry": gpd.points_from_xy(x_coords, y_coords),
        },
        crs="EPSG:4326",
    ).set_index("subbasin_id")

    for group, subbasins in groups.items():
        for subbasin in subbasins:
            rivers_xy.loc[subbasin, "group"] = group

    assert len(groups) > 1, "Expected multiple groups for the given area threshold."

    rivers_xy.plot(column="group", categorical=True, markersize=5)

    plt.title("Subbasin Groups Based on Upstream Area")
    plt.savefig(output_folder / "subbasin_groups.png")


